import argparse
def handle_cli_args():
    parser = argparse.ArgumentParser()
    parser.add_argument("--model_path", type=str, required=True)
    parser.add_argument("--device", type=str, required=True)
    parser.add_argument("--torch_dtype", type=str, required=True)
    parser.add_argument("--acceleration", type=str, required=True)
    parser.add_argument("--server", type=str, required=True)
    parser.add_argument("--config_path", type=str, required=True)
    parser.add_argument("--output_path", type=str, default="output")
    parser.add_argument("--batch_size", type=int, default=1)
    parser.add_argument("--device_split_num", type=int, default=1)
    parser.add_argument("--postprocess", type=str, default="")
    parser.add_argument("--limit", type=str, default=None)
    parser.add_argument("--write_out", action="store_true", default=False)
    parser.add_argument("--eval", action="store_true", default=False)
    parser.add_argument("--null1", action="store_true", default=False)
    parser.add_argument("--null2", action="store_true", default=False)
    parser.add_argument("--generation_path", type=str, default=False)
    args = parser.parse_args()
    return args